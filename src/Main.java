                System.out.println("Byte: " + myByteWrapper);
                System.out.println("Short: " + myShortWrapper);
                System.out.println("Float: " + myFloatWrapper);
                System.out.println("Double: " + myDoubleWrapper);
                System.out.println("Long: " + myLongWrapper);
